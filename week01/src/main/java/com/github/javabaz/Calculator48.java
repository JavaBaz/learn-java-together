package com.github.javabaz;

public class Calculator48 {

    public int printSum(int a, int b) {
        int sum = a + b;
        return sum;
    }
    public int printSubtract(int a, int b) {
        int subtract = a - b;
        return subtract;
    }
    public int printMultiply(int a, int b) {
        int multiply = a * b;
        return multiply;
    }
    public float printDivide(float a, float b) {
        float divide = a / b;
        return divide;
    }
}
